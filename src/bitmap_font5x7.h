inline void set_pixel(std::vector<uint8_t>& img, int w, int h,
                      int x, int y, uint8_t r, uint8_t g, uint8_t b, uint8_t a = 255) {
    if (x < 0 || y < 0 || x >= w || y >= h) return;
    size_t idx = ((size_t)y * w + x) * 4;
    img[idx + 0] = r;
    img[idx + 1] = g;
    img[idx + 2] = b;
    img[idx + 3] = a;
}

inline void fill_rect(std::vector<uint8_t>& img, int w, int h,
                      int x0, int y0, int rw, int rh,
                      uint8_t r, uint8_t g, uint8_t b) {
    for (int y = y0; y < y0 + rh; y++) {
        for (int x = x0; x < x0 + rw; x++) {
            set_pixel(img, w, h, x, y, r, g, b);
        }
    }
}

inline int string_width(const char* text, int scale, int spacing = 1) {
    int len = (int)strlen(text);
    if (len == 0) return 0;
    return len * (5 * scale + spacing) - spacing;
}

inline void draw_char(std::vector<uint8_t>& img, int w, int h,
                      char ch, int x, int y, int scale,
                      uint8_t r, uint8_t g, uint8_t b) {
    const uint8_t* glyph = glyph5x7(ch);
    for (int col = 0; col < 5; col++) {
        uint8_t bits = glyph[col];
        for (int row = 0; row < 7; row++) {
            if (bits & (1 << row)) {
                for (int sy = 0; sy < scale; sy++) {
                    for (int sx = 0; sx < scale; sx++) {
                        set_pixel(img, w, h, x + col * scale + sx, y + row * scale + sy, r, g, b);
                    }
                }
            }
        }
    }
}

inline void draw_string(std::vector<uint8_t>& img, int w, int h,
                        const char* text, int x, int y, int scale,
                        uint8_t r, uint8_t g, uint8_t b, int spacing = 1) {
    int cx = x;
    for (const char* p = text; *p; p++) {
        draw_char(img, w, h, *p, cx, y, scale, r, g, b);
        cx += 5 * scale + spacing;
    }
}

inline int fit_scale_for_cell(const char* text, int cell_w, int cell_h, int spacing = 1) {
    for (int scale = 4; scale >= 1; scale--) {
        int tw = string_width(text, scale, spacing);
        int th = 7 * scale;
        if (tw <= cell_w - 8 && th <= cell_h - 8) return scale;
    }
    return 1;
}

inline void draw_string_in_cell(std::vector<uint8_t>& img, int atlas_w, int atlas_h,
                                const char* text, int cell_x, int cell_y, int cell_w, int cell_h,
                                uint8_t r, uint8_t g, uint8_t b) {
    int scale = fit_scale_for_cell(text, cell_w, cell_h);
    int tw = string_width(text, scale);
    int th = 7 * scale;
    int x = cell_x + (cell_w - tw) / 2;
    int y = cell_y + (cell_h - th) / 2;
    draw_string(img, atlas_w, atlas_h, text, x, y, scale, r, g, b);
}

